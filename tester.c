#include <stdlib.h>
#include <stdio.h>
#include <signal.h>
#include <unistd.h>
#include <sys/wait.h>
#include "parray.h"

#define ASIZE 8


static void
handler(int sig, siginfo_t *si, void* unused)
{
    fprintf(stderr, "Got SIGSEGV at address: %p\n", si->si_addr);
    fflush(stderr);
    exit(1);
}

void test1(void)
{
    struct parray* p = parray_new(sizeof(int), ASIZE);
    int* k;
    for (int i = 0; i < ASIZE; i++)
    {
        k = parray_entry(p, i);
        *k = i + 1000;
    }
    for (int i = 0; i < ASIZE; i++)
    {
        k = parray_entry(p, i);
        printf("i, *k = %d, %d\n", i, *k);
    }
    exit(0);
}

void test2(void)
{
    struct parray* p = parray_new(sizeof(char) * 1024, ASIZE);
    char* k;
    for (int i = 0; i < ASIZE; i++)
    {
        k = parray_entry(p, i);
        for (int j = 0; j < 1024; j++)
        {
            k[j] = '!' + (j % 93);
        }
        k[1023] = '\0';
    }
    for (int i = 0; i < ASIZE; i++)
    {
        k = parray_entry(p, i);
        printf("i, *k = %d, %s\n", i, k);
    }

    k[1024] = '!';
    exit(1);
}

void test3(void)
{
    struct parray* p = parray_new(sizeof(int), 200);
    int* j = parray_entry(p, 43);
    *j = 5;
    printf("test3: First assignment succeeded!\n");
    long* k = parray_entry(p, 17);
    *k = 256l;
    exit(1);
}

void runTest(void (*test) (void))
{
    if (!fork())
    {
        test();
    }
    wait(NULL);
}

int main(void)
{
    struct sigaction sa;
    sa.sa_flags = SA_SIGINFO;
    sa.sa_sigaction = &handler;
    /*
     * TODO: Overwrite the signal handler for
     * SIGSEGV
     */
    if (sigaction(SIGSEGV, &sa, NULL) < 0 ) {
      fprintf(stderr, "SIGACTION ERROR");
      return -1;
    }
    runTest(test1);
    runTest(test2);
    runTest(test3);
    return 0;
}

